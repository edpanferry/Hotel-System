    private boolean validPayment(String s,JTextField textField){
        for(char c: s.toCharArray()){
            if((!Character.isDigit(c))&&c!='.') {   
                payment_error.setText("Please input valid payment.");
                payment.setText("");
                return true;
            }
            else{
                payment_error.setText("");
            }
        }
        return false;
    }
   
