    private boolean validContact(String s,JTextField textField){
        for(char c: s.toCharArray()){
            if((!Character.isDigit(c))) {   
                contact_error.setText("Please input valid contact.");
                contact.setText("");
                return true;
            }
            else{
                contact_isGood=true;
                contact_error.setText("");
            }
            }
        return false;
    }
    
