    private boolean complete(){
        boolean allFields=false;
       
        int done=0;
        
        if(male.isSelected()){
            gen="Male";
        }if(female.isSelected()){
            gen="Female";
        }
        if(gen==null)
            gender_error.setText("Gender is required!");
        else{
            gender_error.setText("");
            done++;
        }
        if(name.getText().isEmpty())
            name_error.setText("Name is required!");
        else{
            name_error.setText("");
            done++;
        }
        if(address.getText().isEmpty())
            address_error.setText("Address is required!");
        else{
            address_error.setText("");
            done++;
        }
        if(contact.getText().isEmpty())
            contact_error.setText("Contact is required!");
        else{
            contact_error.setText("");
            done++;
        }

        if(age.getText().isEmpty())
            age_error.setText("Age is required!");
        else{
            if(age_isGood==true)
                done++;
        }
        if(rtype.getSelectedItem().equals("--- Select Type ---"))
            rtype_error.setText("Please choose room type!");
        else{
            rtype_error.setText("");
            done++;
        }
           
        if(zipcode.getText().isEmpty())
            zipcode_error.setText("Zipcode is required!");
        else{
            zipcode_error.setText("");
            done++;
        }
     
        if(done==7) 
            allFields=true;
       
        return allFields;
    }
